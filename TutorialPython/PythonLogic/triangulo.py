# Leia a base e altura de um triangulo, calcule e apresente o valor da area: area = (base * altura) / 2

# Entrada
print("Digite o valor da Altura: ")
altura = float(input())
print("Digite o valor da Largura: ")
largura = float(input())
# processamento
area = (largura * altura) / 2
# Saida
print("Mostrar area: ", area)

